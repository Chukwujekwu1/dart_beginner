void main() {
  // for loop
  int age = 10;
  for (var i = 0; i < age; i++) {
    print(i);
  }

  // for in loop
  var names = ['John', 'Tina', 'Steve'];
  for (var name in names) {
    print(name);
  }

  // while loop
  var height = 10;
  while (height >= 1) {
    print(height);
    height--;
  }
}


